            <h4>&spades;ITパスポート試験との違い</h4>
            <hr align="cener">
            <div>
                まず、共通キャリア・スキルフレームワークにおいてITパスポート試験はレベル１ですが、基本情報技術者は一段上のレベル２です。
                共通キャリア・スキルフレームワークのレベル２では「基本的知識・スキルを有し、一定程度の難易度又は要求された作業について、その一部を独力で遂行できる」能力が要求されるため、すべての分野について範囲や問われる知識の深さが一ランク上になります。
                ITパスポートではテクノロジ系では導入部分しか出題されませんが、基本情報技術者では情報システムを開発するうえで必要となる知識がひと通り問われるので、相当なレベルアップを感じることと思います。
                <br><br>
                顕著な違いとしては、やはり「プログラミング」に関する知識。技能が強く求められることです。２０２２年（令和４年）４月からITパスポート試験でも流れ図やアルゴリズムの種類などプログラミングの基礎的な部分が出題されるようになりましたが、
                基本情報技術者試験で出題されるプログラミング問題はITパスポートとは段違いです。また、２０２３年（令和５年）４月の試験制度改定に伴い、科目B試験（従前は午後試験と呼ばれていました）において２０問中１６問がアルゴリズムとプログラミングの問題となっており、
                これを避けて通ることができません。
                したがって、ITパスポートからレベルアップしようとする人にとってはプログラミング問題（特に科目B試験）が鬼門になることでしょう。
                とはいえ、入門的な資格ですから、<span>初学者でも独学で短期合格吸うことは十分に可能です</span>。
