                        href="#">https://cbt-s.com/examinee/</a>)で、受験者登録を行い、マイページから受験予約を行います(変更は試験日3日前まで可能です)。
                    受験申込みはいつでもできますが、申込可能な試験日は当月から3カ月先の月末まで試験日時までとなります。なお、同一試験で複数の受験日を予約することはできません。
