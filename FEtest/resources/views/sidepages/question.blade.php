                <u class="question">Q. 直ぐに再受験することはできますか？</u>
                <p>A. 同じ試験区分を再受験するには、欠席により受験しなかった場合を除き、前回の受験日から30日間以上の経過を待たなくてはなりません。
                    仮に、4月5日に受験したとしたら、次の受験日として指定できるのは5月6日以降となります。
                    なお、受験申込みは前回の受験日の翌日から行うことができます。
                </p>
            </div>
            <div>
                <u class="question">Q. どの程度であれば掲示板等への投稿が許されますか？</u>
                <p>A. 試験問題内容についての書込みは程度を問わず禁止です。
                    問題は非公開となっており、試験問題の全部または一部(試験問題中に登場する文字に限らず、出題内容を示唆する表現も含む)を第三者に開示(漏洩)しないことが受験する上での契約内容となっております。
                    出題に関するSNSへの投稿、インターネット掲示板への書込みなどについても、上記の開示(漏洩)とみなされることがあります。
                    一方、マイページのスコアレポートや記載されあ内容については投稿可能です。
                    IPAよりスコアレポートや成績通知表(現在は廃止)の内容を第三者に提供することについて、特段制約はないという回答をいただいております。
                </p>
